// for loop - known number of iterations
for (let i = 0; i < 3; i++) {
  console.log("for loop:", i);
}

// while loop - runs while condition is true
let j = 0;
while (j < 3) {
  console.log("while loop:", j);
  j++;
}

// do...while loop - executes at least once
let k = 0;
do {
  console.log("do while loop:", k);
  k++;
} while (k < 3);
